/*     enum 列舉 -->有意義的文字 

   用法:    enum 列舉名稱
                {
                    a,b,c,d,e;
                }
                
            enum 列舉名稱 物件名稱=值          
*/
enum human
{
     a1,a2,a3,a4,a5
};

int main()
{
    enum human a=2000;  
    
    printf("a=%d\n",a);
    
    printf("a1=%d\n",a1);
    printf("a2=%d\n",a2);
    printf("a3=%d\n",a3);
    printf("a4=%d\n",a4);
    printf("a5=%d\n",a5);
   
    system("pause");
    return 0;
}
