int main()
{
    /*for迴圈語法:
    for(初值;進入條件;遞增或遞減)
    {
    
    }
    */
    
       
    /*這個語法的意思是:將"x=0"持續遞增到"x<=25",               
    遞增完成後會將值放入第15行(因為最後"x<=25"符合,會再遞增 
    一次,而遞增後因為不符合進入條件,所以跳出for迴圈,在第21行顯示)*/ 
    
     int x;
     for(x=0;x<=25;x++)
    { 
       printf("%d\n",x);                      
        
    }
    printf("-------%d-------",x);    
    system("pause");
    return 0;
}
